import UIKit

extension UIColor {
    
    class func bananaColor() -> UIColor {
        return UIColor(red:0.98, green:0.86, blue:0.18, alpha:1.00)
    }
    
    class func blueWhaleColor() -> UIColor {
        return UIColor(red:0.12, green:0.19, blue:0.26, alpha:1.00)
    }
    
}